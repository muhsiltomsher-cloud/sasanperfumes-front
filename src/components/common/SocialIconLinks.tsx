export function SocialIconLinks({ links, className = "", variant = "light" }: SocialIconLinksProps) {
  const visibleLinks = links
    .map((link) => ({
      platform: link.platform.trim(),
      url: link.url.trim(),
    }))
    .filter((link) => link.platform && link.url);

  if (visibleLinks.length === 0) return null;

  const sectionClasses =
    variant === "dark"
      ? "border-b border-white/10 bg-[#1a1a1a] py-8"
      : "border-y border-brand-primary/10 bg-white py-8";
  const linkClasses =
    variant === "dark"
      ? "border-white/15 text-white/70 hover:border-white hover:bg-white hover:text-[#1a1a1a]"
      : "border-brand-primary/15 text-brand-primary/70 hover:border-brand-primary hover:bg-brand-primary hover:text-white";

  return (
    <section className={`w-full ${sectionClasses} ${className}`}>
      <div className="flex flex-wrap items-center justify-center gap-4 px-5 md:px-7 lg:px-12">
        {visibleLinks.map((link, index) => {
          const platformKey = normalizePlatform(link.platform);
          const label = platformLabels[platformKey] || link.platform;

          return (
            <a
              key={`${platformKey}-${index}`}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              className={`flex h-11 w-11 items-center justify-center rounded-full border transition-colors ${linkClasses}`}
              aria-label={label}
            >
              <SocialIcon platform={link.platform} />
            </a>
          );
        })}
      </div>
    </section>
  );
}
